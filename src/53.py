def add_numbers(a: int, b: int) -> int:
    """
    Add two integers.
    
    Args:
        a (int): First integer.
        b (int): Second integer.
        
    Returns:
        int: The sum of the two integers.
    """
    return a + b

# Example usage
result = add_numbers(10, 20)
print(result)  # Output: 30
