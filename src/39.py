import numpy as np

def add(x, y):
    """
    Add two numbers x and y.
    
    Parameters:
    x (float): The first number.
    y (float): The second number.
    
    Returns:
    float: The sum of x and y.
    """
    return x + y

def subtract(x, y):
    """
    Subtract one number from another number.
    
    Parameters:
    x (float): The first number.
    y (float): The second number.
    
    Returns:
    float: The difference between x and y.
    """
    return x - y
